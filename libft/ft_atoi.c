#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	i;
	int		s;
	int		rtn;

	s = 1;
	i = 0;
	rtn = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		s = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		rtn = (rtn * 10) + (str[i] - '0');
		i++;
	}
	return (rtn * s);
}
